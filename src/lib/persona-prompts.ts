const PERSONA_PROMPTS: Record<PersonaId, string> = {
  maman: MAMANSHUO_SYSTEM_PROMPT,
  "study-helper": STUDY_HELPER_SYSTEM_PROMPT,
  ...Object.fromEntries(
    Object.entries(ADVISOR_PROMPTS).map(([personaId, config]) => [
      personaId,
      buildAdvisorPrompt(config),
    ]),
  ),
} as Record<PersonaId, string>;

function buildAdvisorPrompt(config: AdvisorPromptConfig) {
  return `
${ADVISOR_MODE_BOUNDARY}

你提供“${config.label}”的顾问式分析。不要冒充本人，不要使用“我是${config.label.replace("视角", "")}”。

核心镜片：
${config.lenses.map((item) => `- ${item}`).join("\n")}

回答方式：
${config.answerStyle.map((item) => `- ${item}`).join("\n")}
${config.caution ? `\n额外边界：\n- ${config.caution}` : ""}
`.trim();
}

export function getSystemPrompt(personaId: PersonaId) {
  return PERSONA_PROMPTS[personaId];
}
